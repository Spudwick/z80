
;====================================================================================
; __pc_into_hl :
;------------------------------------------------------------------------------------
; Expects:
;    N/A
;
; Returns:
;     hl = Program Counter (following call to self).
;====================================================================================
__pc_into_hl::
    ld h,#0
    ld l,#0
    add hl,sp
    ret


